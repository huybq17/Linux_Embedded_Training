/*************************************************************************
 * Include
 ************************************************************************/
 #include <stdio.h>
 #include <stdlib.h>
 #include <unistd.h>
 #include <signal.h>
 #include <sys/types.h>
 #include <sys/wait.h>

/**************************************************************************************************************
* Main program
***************************************************************************************************************/
int main() {
    pid_t child_pid = fork();
    int wstatus = 0;

    if (child_pid < 0) {
        perror("Fork failed");
        exit(1);
    }

    if (0 == child_pid) {
        printf("I'm Child process with PID %d\n", getpid());
    } else {
        sleep(1);
        printf("Parent process...\n");
        wait(&wstatus);
    }

    return 0;
}

